    private void addToFleetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToFleetBtnActionPerformed
        // TODO add your handling code here:
        if (carNameTextField.getText().equals("") || manufacturerNameTextField.getText().equals("") || cityOfOriginTextField.getText().equals("") || modelNumberTextField.getText().equals("") || serialNumberTextField.getText().equals("") || numberOfSeatsTextField.getText().equals("") || yearOfManufactureTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please make sure to enter all the textfields.");
        } else {
            Car car = fleetOfCars.addCar();
            car.setCarName(carNameTextField.getText().trim());
            car.setManufacturer(manufacturerNameTextField.getText().trim());
            car.setCityOfOrigin(cityOfOriginTextField.getText().trim());
            car.setModelNumber(Integer.parseInt(modelNumberTextField.getText().trim()));
            car.setSerialNumber(Integer.parseInt(serialNumberTextField.getText().trim()));
            car.setNumberOfseats(Integer.parseInt(numberOfSeatsTextField.getText().trim()));
            car.setYearOFManufacture(Integer.parseInt(yearOfManufactureTextField.getText().trim()));
            car.setMaintenceCerticateValidity(maintenanceValidCheckBox.isSelected());
            car.setAvailability(availableCheckBox.isSelected());
            DateFormat dateFormat = new SimpleDateFormat("MM/dd/yy HH:mm:ss");
            Calendar calendar = Calendar.getInstance();
            fleetOfCars.setLastModified(dateFormat.format(calendar.getTime()));
            JOptionPane.showMessageDialog(null, "Car Added Successfully To Fleet");
            carNameTextField.setText("");
            manufacturerNameTextField.setText("");
            cityOfOriginTextField.setText("");
            modelNumberTextField.setText("");
            serialNumberTextField.setText("");
            numberOfSeatsTextField.setText("");
            yearOfManufactureTextField.setText("");
            maintenanceValidCheckBox.setSelected(false);
            availableCheckBox.setSelected(false);
        }
    }//GEN-LAST:event_addToFleetBtnActionPerformed

