    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        int rowNumber = tableVitalSign.getSelectedRow();
        if (rowNumber >= 0) {
            VitalSigns vitalSigns = (VitalSigns) tableVitalSign.getValueAt(rowNumber, 0);
            temperatureTextField.setText(String.valueOf(vitalSigns.getTemperature()));
            temperatureTextField.setEnabled(false);
            bloodPressureTextField.setText(String.valueOf(vitalSigns.getBloodPressure()));
            bloodPressureTextField.setEnabled(false);
            pulseTextField.setText(String.valueOf(vitalSigns.getPulse()));
            pulseTextField.setEnabled(false);
            dateTextField.setText(vitalSigns.getDate());
            dateTextField.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_viewBtnActionPerformed

    public void setDefaultTable(double max, double min) {
        list = vitalSignHistory.getAbnormalList(max, min);
        intialTextFileds();
        populateTable(list);
    }

    private void intialTextFileds() {
        temperatureTextField.setText("");
        bloodPressureTextField.setText("");
        pulseTextField.setText("");
        dateTextField.setText("");
    }

    public void populateTable(ArrayList<VitalSigns> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tableVitalSign.getModel();
        defaultTableModel.setRowCount(0);
        for (VitalSigns vs : list) {
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            defaultTableModel.addRow(row);
        }
    }

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int rowNumber = tableVitalSign.getSelectedRow();
        if (rowNumber >= 0) {
            VitalSigns vitalSigns = (VitalSigns) tableVitalSign.getValueAt(rowNumber, 0);
            vitalSignHistory.deleteVitals(vitalSigns);
            list.remove(vitalSigns);
            JOptionPane.showMessageDialog(null, "Vital sign deleted");
            populateTable(list);
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


