    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        try {
            Double.parseDouble(priceTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Price cannot be empty or have alphabets in it.");
            return;
        }
        try {
            Integer.parseInt(availabilityTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Availability cannot be empty or have alphabet or decimal in it.");
            return;
        }
        try {
            Integer.parseInt(productNameTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Product Number cannot be empty or have alphabet or decimal in it.");
            return;
        }
            Product product = productCatalog.addProduct();
            product.setProductName(Integer.parseInt(productNameTextField.getText()));
            product.setPrice(Double.parseDouble(priceTextField.getText()));
            product.setAvailability(Integer.parseInt(availabilityTextField.getText()));
            product.setDescription(descriptionTextField.getText());
            JOptionPane.showMessageDialog(null, "Product Added to Catalog Successfully");
            priceTextField.setText("");
            productNameTextField.setText("");
            descriptionTextField.setText("");
            availabilityTextField.setText("");
       
    }//GEN-LAST:event_createBtnActionPerformed

